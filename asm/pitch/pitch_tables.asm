; ==========================================================================
; PITCH TABLES - 4 Octaves (48 Notes)
; ==========================================================================
; 8.8 fixed-point pitch multipliers for equal temperament tuning.
;
; Formula: pitch[n] = 256 * 2^(n/12) where n=0 is C-1
;
; Mapping (GUI to Pitch Table):
;   +---------+-------+-------+------------+
;   | GUI     | Note  | Index | Multiplier |
;   +---------+-------+-------+------------+
;   | C-1     |   1   |   0   |   1.0x     |
;   | C-2     |  13   |  12   |   2.0x     |
;   | C-3     |  25   |  24   |   4.0x     |
;   | C-4     |  37   |  36   |   8.0x     |
;   +---------+-------+-------+------------+
;
; The ASM player converts: pitch_index = gui_note - 1
; ==========================================================================

NOTE_PITCH_LO:
    ; --- Octave 1 (C-1 to B-1) - Base octave, 1.0x pitch ---
    .byte $00   ; C-1  = $0100 = 256 = 1.000x
    .byte $0F   ; C#1  = $010F = 271 = 1.059x
    .byte $1F   ; D-1  = $011F = 287 = 1.122x
    .byte $30   ; D#1  = $0130 = 304 = 1.189x
    .byte $43   ; E-1  = $0143 = 323 = 1.260x
    .byte $56   ; F-1  = $0156 = 342 = 1.335x
    .byte $6B   ; F#1  = $016B = 363 = 1.414x
    .byte $80   ; G-1  = $0180 = 384 = 1.498x
    .byte $97   ; G#1  = $0197 = 407 = 1.587x
    .byte $B0   ; A-1  = $01B0 = 432 = 1.682x
    .byte $CA   ; A#1  = $01CA = 458 = 1.782x
    .byte $E5   ; B-1  = $01E5 = 485 = 1.888x
    
    ; --- Octave 2 (C-2 to B-2) - 2.0x pitch ---
    .byte $00   ; C-2  = $0200 = 512 = 2.000x
    .byte $1E   ; C#2  = $021E = 542 = 2.117x
    .byte $3E   ; D-2  = $023E = 574 = 2.242x
    .byte $60   ; D#2  = $0260 = 608 = 2.378x
    .byte $85   ; E-2  = $0285 = 645 = 2.520x
    .byte $AC   ; F-2  = $02AC = 684 = 2.670x
    .byte $D6   ; F#2  = $02D6 = 726 = 2.835x
    .byte $00   ; G-2  = $0300 = 768 = 3.000x
    .byte $2E   ; G#2  = $032E = 814 = 3.180x
    .byte $60   ; A-2  = $0360 = 864 = 3.375x
    .byte $94   ; A#2  = $0394 = 916 = 3.578x
    .byte $CC   ; B-2  = $03CC = 972 = 3.797x
    
    ; --- Octave 3 (C-3 to B-3) - 4.0x pitch ---
    .byte $00   ; C-3  = $0400 = 1024 = 4.000x
    .byte $3C   ; C#3  = $043C = 1084 = 4.234x
    .byte $7C   ; D-3  = $047C = 1148 = 4.484x
    .byte $C0   ; D#3  = $04C0 = 1216 = 4.750x
    .byte $0A   ; E-3  = $050A = 1290 = 5.039x
    .byte $58   ; F-3  = $0558 = 1368 = 5.344x
    .byte $AC   ; F#3  = $05AC = 1452 = 5.672x
    .byte $00   ; G-3  = $0600 = 1536 = 6.000x
    .byte $5C   ; G#3  = $065C = 1628 = 6.359x
    .byte $C0   ; A-3  = $06C0 = 1728 = 6.750x
    .byte $28   ; A#3  = $0728 = 1832 = 7.156x
    .byte $98   ; B-3  = $0798 = 1944 = 7.594x
    
    ; --- Octave 4 (C-4 to B-4) - 8.0x pitch (highest) ---
    .byte $00   ; C-4  = $0800 = 2048 = 8.000x
    .byte $78   ; C#4  = $0878 = 2168 = 8.469x
    .byte $F8   ; D-4  = $08F8 = 2296 = 8.969x
    .byte $80   ; D#4  = $0980 = 2432 = 9.500x
    .byte $14   ; E-4  = $0A14 = 2580 = 10.078x
    .byte $B0   ; F-4  = $0AB0 = 2736 = 10.688x
    .byte $58   ; F#4  = $0B58 = 2904 = 11.344x
    .byte $00   ; G-4  = $0C00 = 3072 = 12.000x
    .byte $B8   ; G#4  = $0CB8 = 3256 = 12.719x
    .byte $80   ; A-4  = $0D80 = 3456 = 13.500x
    .byte $50   ; A#4  = $0E50 = 3664 = 14.313x
    .byte $30   ; B-4  = $0F30 = 3888 = 15.188x

NOTE_PITCH_HI:
    ; --- Octave 1 (C-1 to B-1) ---
    .byte $01,$01,$01,$01,$01,$01,$01,$01,$01,$01,$01,$01
    
    ; --- Octave 2 (C-2 to B-2) ---
    .byte $02,$02,$02,$02,$02,$02,$02,$03,$03,$03,$03,$03
    
    ; --- Octave 3 (C-3 to B-3) ---
    .byte $04,$04,$04,$04,$05,$05,$05,$06,$06,$06,$07,$07
    
    ; --- Octave 4 (C-4 to B-4) ---
    .byte $08,$08,$08,$09,$0A,$0A,$0B,$0C,$0C,$0D,$0E,$0F
